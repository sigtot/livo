Pose3 NewerCollegeGroundTruth::At(double timestamp) {
  // Yes, we could for sure do something more fancy and more performant here.
  // We could bisect or create a map of frame id's to poses, but since we're
  // really only going to use this gt table for very early experiments, this
  // linear search will do.
  for (auto& gt_it : gt) {
    if (std::abs(gt_it.first - timestamp) < THRESH) {
      return gt_it.second;
    }
  }
  std::cout << "Did not find any ground truth pose with timestamp close to "
            << std::setprecision(20) << timestamp << std::endl;
  std::cout << "Note that the gt table only contains the 100 first poses"
            << std::endl;
  exit(1);
}

